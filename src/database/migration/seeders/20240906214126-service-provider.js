// Utility function to generate operating hours for all days
function generateOperatingHours(dayShifts) {
  const operatingHours = {
    days: {},
  };
  const allDays = ['monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday', 'sunday'];
  allDays.forEach((day) => {
    const shiftData = dayShifts.find((ds) => ds.days.includes(day)) || { shifts: [] };
    operatingHours.days[day] = {
      open: shiftData.shifts.length > 0,
      shifts: shiftData.shifts,
      timeSlots: generateTimeSlots(shiftData.shifts),
    };
  });
  return operatingHours;
}

// Utility function to generate time slots based on shifts
function generateTimeSlots(shifts) {
  const timeSlots = [];
  shifts.forEach(({ start, end }) => {
    let currentTime = start;
    while (currentTime < end) {
      timeSlots.push(currentTime);
      currentTime = addMinutes(currentTime, 30); // Increment by 30 minutes
    }
  });
  return timeSlots;
}

// Utility function to add minutes to a time string
function addMinutes(time, minutes) {
  const [hours, mins] = time.split(':').map(Number);
  const totalMinutes = hours * 60 + mins + minutes;
  const newHours = Math.floor(totalMinutes / 60)
    .toString()
    .padStart(2, '0');
  const newMinutes = (totalMinutes % 60).toString().padStart(2, '0');
  return `${newHours}:${newMinutes}`;
}
